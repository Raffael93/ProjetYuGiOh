public class Cards {

    public int id;
    public String name;
    public String url;
    public String desc;
    public String type;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }

    public String getDesc() {
        return desc;
    }

    public String getType() {
        return type;
    }
}
