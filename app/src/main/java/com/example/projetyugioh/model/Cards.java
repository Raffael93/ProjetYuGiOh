package com.example.projetyugioh.Model;

public class Cards {

    private int id;
    private String name;
    private String url;
    private String desc;
    private String type;
    private String atk;
    private String def;

    public String getAtk() {
        return atk;
    }

    public String getDef() {
        return def;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }

    public String getDesc() {
        return desc;
    }

    public String getType() {
        return type;
    }
}
